print("Ejercicio 1 - Encuesta de Edades")
for i in range (1, 6):
    edad = int(input("Ingrese la edad de una de las personas: "))
    while edad < 0 :
        print("La edad no puede ser negativa.")
        edad = int(input("Ingrese la edad correcta: "))
    if edad < 18:
        print("La persona es menor de edad.")
    elif edad < 66:
        print("La persona es adulta.")
    else:
        print("La persona es vieja.")